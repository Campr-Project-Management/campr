<?php

namespace AppBundle\Tests\Controller\Admin;

use AppBundle\Entity\ProjectModule;
use MainBundle\Tests\Controller\BaseController;
use Symfony\Component\DomCrawler\Crawler;
use Symfony\Component\HttpFoundation\Request;
use Symfony\Component\HttpFoundation\Response;

class ProjectModuleControllerTest extends BaseController
{
    public function testFormIsDisplayedOnCreatePage()
    {
        $this->login();

        /** @var Crawler $crawler */
        $crawler = $this->client->request(Request::METHOD_GET, '/admin/project-module/create');

        $this->assertContains('id="create_module"', $crawler->html());
        $this->assertContains('name="create[module]"', $crawler->html());
        $this->assertContains('id="create_project"', $crawler->html());
        $this->assertContains('name="create[project]"', $crawler->html());
        $this->assertContains('id="create_isEnabled"', $crawler->html());
        $this->assertContains('name="create[isEnabled]"', $crawler->html());
        $this->assertContains('id="create_isRequired"', $crawler->html());
        $this->assertContains('name="create[isRequired]"', $crawler->html());
        $this->assertContains('id="create_isPrint"', $crawler->html());
        $this->assertContains('name="create[isPrint]"', $crawler->html());
        $this->assertContains('type="submit"', $crawler->html());

        $this->assertSame(Response::HTTP_OK, $this->client->getResponse()->getStatusCode());
    }

    public function testCreateAction()
    {
        $this->login();

        $crawler = $this->client->request(Request::METHOD_GET, '/admin/project-module/create');

        $form = $crawler->filter('#create-form')->first()->form();
        $form['create[module]'] = 'todos';

        $this->client->submit($form);
        $this->assertTrue($this->client->getResponse()->isRedirect());

        $this->client->followRedirect();
        $this->assertContains('Project module successfully created!', $this->client->getResponse()->getContent());

        $projectModule = $this
            ->em
            ->getRepository(ProjectModule::class)
            ->findOneBy([
                'module' => 'todos',
            ])
        ;
        $this->em->remove($projectModule);
        $this->em->flush();
    }

    public function testDeleteAction()
    {
        $this->login();

        $projectModule = (new ProjectModule())
            ->setModule('plan')
            ->setIsEnabled(true)
        ;
        $this->em->persist($projectModule);
        $this->em->flush();

        $this->client->request(Request::METHOD_GET, sprintf('/admin/project-module/%d/delete', $projectModule->getId()));
        $this->assertTrue($this->client->getResponse()->isRedirect());

        $this->client->followRedirect();
        $this->assertContains('Project module successfully deleted!', $this->client->getResponse()->getContent());
    }

    public function testFormIsDisplayedOnEditPage()
    {
        $this->login();

        /** @var Crawler $crawler */
        $crawler = $this->client->request(Request::METHOD_GET, '/admin/project-module/1/edit');

        $this->assertContains('id="create_module"', $crawler->html());
        $this->assertContains('name="create[module]"', $crawler->html());
        $this->assertContains('id="create_project"', $crawler->html());
        $this->assertContains('name="create[project]"', $crawler->html());
        $this->assertContains('id="create_isEnabled"', $crawler->html());
        $this->assertContains('name="create[isEnabled]"', $crawler->html());
        $this->assertContains('id="create_isRequired"', $crawler->html());
        $this->assertContains('name="create[isRequired]"', $crawler->html());
        $this->assertContains('id="create_isPrint"', $crawler->html());
        $this->assertContains('name="create[isPrint]"', $crawler->html());
        $this->assertContains('type="submit"', $crawler->html());
        $this->assertContains('class="zmdi zmdi-delete"', $crawler->html());

        $this->assertSame(Response::HTTP_OK, $this->client->getResponse()->getStatusCode());
    }

    public function testEditAction()
    {
        $this->login();

        $crawler = $this->client->request(Request::METHOD_GET, '/admin/project-module/2/edit');

        $form = $crawler->filter('#edit-form')->first()->form();
        $form['create[module]'] = 'contract';

        $this->client->submit($form);
        $this->assertTrue($this->client->getResponse()->isRedirect());

        $this->client->followRedirect();
        $this->assertContains('Project module successfully edited!', $this->client->getResponse()->getContent());
    }

    public function testDataTableOnListPage()
    {
        $this->login();

        /** @var Crawler $crawler */
        $crawler = $this->client->request(Request::METHOD_GET, '/admin/project-module/list');

        $this->assertEquals(1, $crawler->filter('#data-table-command')->count());
        $this->assertContains('data-column-id="id"', $crawler->html());
        $this->assertContains('data-column-id="module"', $crawler->html());
        $this->assertContains('data-column-id="projectName"', $crawler->html());
        $this->assertContains('data-column-id="createdAt"', $crawler->html());
        $this->assertContains('data-column-id="updatedAt"', $crawler->html());
        $this->assertContains('data-column-id="commands"', $crawler->html());
        $this->assertEquals(1, $crawler->filter('.zmdi-plus')->count());

        $this->assertSame(Response::HTTP_OK, $this->client->getResponse()->getStatusCode());
    }

    public function testTableStructureOnShowAction()
    {
        $this->login();

        $crawler = $this->client->request(Request::METHOD_GET, '/admin/project-module/1/show');

        $this->assertEquals(1, $crawler->filter('.dropdown-menu-right')->count());
        $this->assertEquals(1, $crawler->filter('.table-responsive')->count());
        $this->assertEquals(1, $crawler->filter('.table-striped')->count());

        $this->assertSame(Response::HTTP_OK, $this->client->getResponse()->getStatusCode());
    }
}
